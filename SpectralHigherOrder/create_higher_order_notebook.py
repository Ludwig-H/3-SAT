    "def project_edge_spins_to_vertices(A, p_opt, edges_list, y_cand, total_nodes):\n",
    "    W = np.array([abs(A[u, v]) for u, v in edges_list], dtype=np.float64)\n",
    "    c_e = W * np.abs(1.0 - 2.0 * p_opt)\n",
    "    \n",
    "    R = np.zeros((total_nodes, total_nodes), dtype=np.float64)\n",
    "    for idx, (u, v) in enumerate(edges_list):\n",
    "        epsilon_e = np.sign(A[u, v])\n",
    "        val = c_e[idx] * epsilon_e * y_cand[idx]\n",
    "        R[u, v] = val\n",
    "        R[v, u] = val\n",
    "        \n",
    "    d = np.sum(np.abs(R), axis=1)\n",
    "    if np.sum(d) < 1e-9:\n",
    "        return np.ones(total_nodes)\n",
    "        \n",
    "    d_inv_sqrt = np.zeros_like(d)\n",
    "    mask = d > 1e-9\n",
    "    d_inv_sqrt[mask] = 1.0 / np.sqrt(d[mask])\n",
    "    \n",
    "    D_inv_sqrt = scipy.sparse.diags(d_inv_sqrt)\n",
    "    D_sparse = scipy.sparse.diags(d)\n",
    "    L_sync = D_sparse - scipy.sparse.csc_matrix(R)\n",
    "    L_sync_norm = D_inv_sqrt @ L_sync @ D_inv_sqrt\n",
    "    \n",
    "    try:\n",
    "        if total_nodes <= 100:\n",
    "            eigenvalues, eigenvectors = np.linalg.eigh(L_sync_norm.toarray())\n",
    "            idx_min = np.argmin(eigenvalues)\n",
    "            v_min = eigenvectors[:, idx_min]\n",
    "        else: \n",
    "            eigenvalues, eigenvectors = scipy.sparse.linalg.eigsh(L_sync_norm, k=1, sigma=-1e-5, which='LM')\n",
    "            v_min = eigenvectors[:, 0]\n",
