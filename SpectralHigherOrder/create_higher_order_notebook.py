    "def solve_edge_space_qp(A, omega, rho, edges_list, edge_to_idx, triangles, kappa=1.0):\n",
    "    Ne = len(edges_list)\n",
    "    Nt = len(triangles)\n",
    "    W = np.array([abs(A[u, v]) for u, v in edges_list], dtype=np.float64)\n",
    "    \n",
    "    if Ne == 0:\n",
    "        return np.zeros(0)\n",
    "    if Nt == 0:\n",
    "        return np.zeros(Ne)\n",
    "        \n",
    "    tri_edge_indices = []\n",
    "    b = np.zeros(Nt, dtype=np.float64)\n",
    "    \n",
    "    for t_idx, (i, j, k) in enumerate(triangles):\n",
    "        e1 = (i, j) if (i, j) in edge_to_idx else (j, i)\n",
    "        e2 = (j, k) if (j, k) in edge_to_idx else (k, j)\n",
